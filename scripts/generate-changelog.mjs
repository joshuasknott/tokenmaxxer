import { execFile } from "node:child_process";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);

const outputJsonPath = path.join("public", "changelog.json");
const outputTsPath = path.join("src", "generated", "changelog.ts");
const outputMarkdownPath = "CHANGELOG.md";

async function runGit(args) {
  try {
    const { stdout } = await execFileAsync("git", args, {
      encoding: "utf8",
      maxBuffer: 1024 * 1024 * 8,
    });
    return stdout.trim();
  } catch {
    return "";
  }
}

function titleForVersion(version) {
  return version === "Next" ? "Next release" : `TokenMaxxer ${version}`;
}

function cleanSubject(subject) {
  return subject
    .replace(/^\w+(?:\([^)]+\))?!?:\s*/, "")
    .replace(/^[-*\s]+/, "")
    .trim();
}

function categoryForSubject(subject) {
  const normalized = subject.toLowerCase();

  if (/(fix|repair|bug|error|crash|broken)/.test(normalized)) {
    return "Fixes";
  }

  if (/(release|package|packaging|tauri|updater|workflow|ci|build|bundle|macos|linux|windows|artifact)/.test(normalized)) {
    return "Packaging";
  }

  if (/(doc|readme|license)/.test(normalized)) {
    return "Docs";
  }

  if (/(add|wire|refresh|improve|redesign|polish|prepare|create|support|page|site|provider)/.test(normalized)) {
    return "Improvements";
  }

  if (/changelog/.test(normalized)) {
    return "Docs";
  }

  return "Changes";
}

function groupCommits(subjects) {
  const grouped = new Map();

  for (const rawSubject of subjects) {
    const subject = cleanSubject(rawSubject);
    if (!subject || /^merge /i.test(subject)) continue;

    const category = categoryForSubject(subject);
    const items = grouped.get(category) ?? [];
    items.push(subject);
    grouped.set(category, items);
  }

  const order = ["Improvements", "Fixes", "Packaging", "Docs", "Changes"];
  return order
    .filter((category) => grouped.has(category))
    .map((category) => ({
      category,
      items: [...new Set(grouped.get(category))],
    }));
}

async function commitsInRange(range) {
  const raw = await runGit(["log", "--reverse", "--format=%s", range]);
  return raw ? raw.split("\n").filter(Boolean) : [];
}

async function dateForRef(ref) {
  return runGit(["log", "-1", "--format=%as", ref]);
}

async function buildEntries() {
  const packageJson = JSON.parse(await readFile("package.json", "utf8"));
  const tagsRaw = await runGit(["tag", "--list", "v*", "--sort=creatordate"]);
  const tags = tagsRaw ? tagsRaw.split("\n").filter(Boolean) : [];
  const entries = [];

  const latestTag = tags.at(-1);
  const nextSubjects = await commitsInRange(latestTag ? `${latestTag}..HEAD` : "HEAD");
  if (nextSubjects.length > 0) {
    entries.push({
      version: "Next",
      title: titleForVersion("Next"),
      date: await dateForRef("HEAD"),
      summary: latestTag
        ? `Changes queued after ${latestTag}. This section refreshes from commit history when the site is built.`
        : "Changes queued for the first tagged release.",
      groups: groupCommits(nextSubjects),
    });
  }

  for (let index = tags.length - 1; index >= 0; index -= 1) {
    const tag = tags[index];
    const previousTag = tags[index - 1];
    const subjects = await commitsInRange(previousTag ? `${previousTag}..${tag}` : tag);
    const version = tag.replace(/^v/, "");

    entries.push({
      version,
      title: titleForVersion(version),
      date: await dateForRef(tag),
      summary: `${titleForVersion(version)} release notes generated from repository history.`,
      groups: groupCommits(subjects),
    });
  }

  if (entries.length === 0) {
    entries.push({
      version: packageJson.version,
      title: titleForVersion(packageJson.version),
      date: "",
      summary: "Current TokenMaxxer package version.",
      groups: [
        {
          category: "Changes",
          items: ["Initial changelog entry."],
        },
      ],
    });
  }

  return {
    schemaVersion: 1,
    packageVersion: packageJson.version,
    entries,
  };
}

function markdownFor(data) {
  const lines = [
    "# Changelog",
    "",
    "This file is generated by `scripts/generate-changelog.mjs` from version tags and commit history.",
    "Run `pnpm changelog:update` before publishing when you want to refresh the committed copy.",
    "",
  ];

  for (const entry of data.entries) {
    lines.push(`## ${entry.title}${entry.date ? ` - ${entry.date}` : ""}`);
    lines.push("");
    lines.push(entry.summary);
    lines.push("");

    for (const group of entry.groups) {
      lines.push(`### ${group.category}`);
      for (const item of group.items) {
        lines.push(`- ${item}`);
      }
      lines.push("");
    }
  }

  return `${lines.join("\n").trim()}\n`;
}

const data = await buildEntries();
const json = `${JSON.stringify(data, null, 2)}\n`;

await mkdir(path.dirname(outputJsonPath), { recursive: true });
await mkdir(path.dirname(outputTsPath), { recursive: true });

await writeFile(outputJsonPath, json);
await writeFile(
  outputTsPath,
  `const changelog = ${json.trim()} as const;\n\nexport default changelog;\n`
);
await writeFile(outputMarkdownPath, markdownFor(data));
