import { execFile } from "node:child_process";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);

const outputJsonPath = path.join("public", "changelog.json");
const outputTsPath = path.join("src", "generated", "changelog.ts");
const outputMarkdownPath = "CHANGELOG.md";
const curatedSourcePath = "changelog.source.json";

async function runGit(args) {
  try {
    const { stdout } = await execFileAsync("git", args, {
      encoding: "utf8",
      maxBuffer: 1024 * 1024 * 8,
    });
    return stdout.trim();
  } catch {
    return "";
  }
}

function titleForVersion(version) {
  return `TokenMaxxer ${version}`;
}

async function loadCuratedNotes() {
  try {
    const raw = await readFile(curatedSourcePath, "utf8");
    return JSON.parse(raw);
  } catch {
    return {};
  }
}

async function dateForRef(ref) {
  return runGit(["log", "-1", "--format=%as", ref]);
}

async function buildEntries() {
  const packageJson = JSON.parse(await readFile("package.json", "utf8"));
  const tagsRaw = await runGit(["tag", "--list", "v*", "--sort=creatordate"]);
  const tags = tagsRaw ? tagsRaw.split("\n").filter(Boolean) : [];
  const curated = await loadCuratedNotes();
  const entries = [];

  for (let index = tags.length - 1; index >= 0; index -= 1) {
    const tag = tags[index];
    const version = tag.replace(/^v/, "");
    const curatedEntry = curated[version];

    if (!curatedEntry) continue;

    entries.push({
      version,
      title: titleForVersion(version),
      date: await dateForRef(tag),
      summary: curatedEntry.summary,
      notes: curatedEntry.notes,
    });
  }

  return {
    schemaVersion: 2,
    packageVersion: packageJson.version,
    entries,
  };
}

function markdownFor(data) {
  const lines = [
    "# Changelog",
    "",
    "This file is generated by `scripts/generate-changelog.mjs` from version tags and curated release notes.",
    "Run `pnpm changelog:update` before publishing when you want to refresh the committed copy.",
    "",
  ];

  for (const entry of data.entries) {
    lines.push(`## ${entry.title}${entry.date ? ` - ${entry.date}` : ""}`);
    lines.push("");
    lines.push(entry.summary);
    lines.push("");

    for (const note of entry.notes) {
      lines.push(`- ${note}`);
    }
    lines.push("");
  }

  if (data.entries.length === 0) {
    lines.push("No public TokenMaxxer releases have been tagged yet.");
    lines.push("");
  }

  return `${lines.join("\n").trim()}\n`;
}

const data = await buildEntries();
const json = `${JSON.stringify(data, null, 2)}\n`;

await mkdir(path.dirname(outputJsonPath), { recursive: true });
await mkdir(path.dirname(outputTsPath), { recursive: true });

await writeFile(outputJsonPath, json);
await writeFile(
  outputTsPath,
  `type ChangelogEntry = {\n  version: string;\n  title: string;\n  date: string;\n  summary: string;\n  notes: string[];\n};\n\ntype ChangelogData = {\n  schemaVersion: 2;\n  packageVersion: string;\n  entries: ChangelogEntry[];\n};\n\nconst changelog: ChangelogData = ${json.trim()};\n\nexport default changelog;\n`
);
await writeFile(outputMarkdownPath, markdownFor(data));
